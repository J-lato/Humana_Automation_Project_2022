package Day_2;

public class T1_LinerArray {

    public static void main(String[] args) {

        //declare a String array variable
        String[] zipCode, streetNumber;
        //defining zipcode values for linear string array
        zipCode = new String[]{"11281","11222","11293","10987"};
        //defining streetNumber values for the linear string array
        streetNumber = new String[]{"129","283","293"};
        //print a value from the zipcode variable. Array always starts from 0 index
        System.out.println("zipcode " + zipCode[3] + " " + zipCode[2] + " " + streetNumber[0]);


    }//end of main
}//end of java class
